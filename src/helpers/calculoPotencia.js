export function calcularPotenciaPlanas(largo, ancho) {

    const potencia = ((largo * ancho) * 4) / 100

    return potencia

}

export function calcularPotenciaSunchos(diametro, ancho) {

    const potencia = (((diametro * 3.14) * ancho) * 4) / 100

    return potencia

}

export function calcularPotenciaCartuchos(largo, diametro) {

    const potencia = ((largo * (diametro * 3.14)) * 4) / 100

    return potencia

}

